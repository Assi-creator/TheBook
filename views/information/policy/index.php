                        1) <font size="4">ОБЩИЕ ПОЛОЖЕНИЯ</font></h1><br>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Политика обработки персональных данных (далее – Политика) разработана в соответствии с
                        Федеральным законом от 27.07.2006. №152-ФЗ «О персональных данных» (далее – ФЗ-152).</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Настоящая Политика определяет порядок обработки персональных данных и меры по обеспечению
                        безопасности персональных данных в ООО «TheBook» (далее – Оператор) с целью защиты прав
                        и свобод человека и гражданина при обработке его персональных данных, в том числе защиты прав на
                        неприкосновенность частной жизни, личную и семейную тайну.</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><a
                            name="ll-_GoBack"></a><br>
                        В Политике используются следующие основные понятия:</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        <b>автоматизированная обработка персональных данных</b> – обработка персональных данных с
                        помощью средств вычислительной техники;</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        <b>блокирование персональных данных</b> - временное прекращение обработки персональных данных
                        (за исключением случаев, если обработка необходима для уточнения персональных данных);</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        <b>информационная система персональных данных</b> - совокупность содержащихся в базах данных
                        персональных данных, и обеспечивающих их обработку информационных технологий и технических
                        средств;</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        <b>обезличивание персональных данных</b><i> </i>- действия, в результате которых невозможно
                        определить без использования дополнительной информации принадлежность персональных данных
                        конкретному субъекту персональных данных;</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        <b>обработка персональных данных</b> - любое действие (операция) или совокупность действий
                        (операций), совершаемых с использованием средств автоматизации или без использования таких
                        средств с персональными данными, включая сбор, запись, систематизацию, накопление, хранение,
                        уточнение (обновление, изменение), извлечение, использование, передачу (распространение,
                        предоставление, доступ), обезличивание, блокирование, удаление, уничтожение персональных данных;
                    </p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        <b>оператор -</b> государственный орган, муниципальный орган, юридическое или физическое лицо,
                        самостоятельно или совместно с другими лицами организующие и (или) осуществляющие обработку
                        персональных данных, а также определяющие цели обработки персональных данных, состав
                        персональных данных, подлежащих обработке, действия (операции), совершаемые с персональными
                        данными;</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        <b>персональные данные</b> – любая информация, относящаяся к прямо или косвенно определенному
                        или определяемому физическому лицу (субъекту персональных данных);</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        <b>предоставление персональных данных</b> – действия, направленные на раскрытие персональных
                        данных определенному лицу или определенному кругу лиц;</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        <b>распространение персональных данных</b> - действия, направленные на раскрытие персональных
                        данных неопределенному кругу лиц (передача персональных данных) или на ознакомление с
                        персональными данными неограниченного круга лиц, в том числе обнародование персональных данных в
                        средствах массовой информации, размещение в информационно-телекоммуникационных сетях или
                        предоставление доступа к персональным данным каким-либо иным способом;</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        <b>трансграничная передача персональных данных</b> - передача персональных данных на территорию
                        иностранного государства органу власти иностранного государства, иностранному физическому или
                        иностранному юридическому лицу;</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        <b>уничтожение персональных данных</b> - действия, в результате которых невозможно восстановить
                        содержание персональных данных в информационной системе персональных данных и (или) результате
                        которых уничтожаются материальные носители персональных данных.</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Компания обязана опубликовать или иным образом обеспечить неограниченный доступ к настоящей
                        Политике обработки персональных данных в соответствии с ч.&nbsp;2 ст.&nbsp;18.1. ФЗ-152.</p>
