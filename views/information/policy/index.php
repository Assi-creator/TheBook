                        2.2 <font size="4">Условия обработки персональных данных</font></h2><br>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Оператор производит обработку персональных данных при наличии хотя бы одного из следующих
                        условий:</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработка персональных данных осуществляется с согласия субъекта персональных данных на
                        обработку его персональных данных;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработка персональных данных необходима для достижения целей, предусмотренных международным
                        договором Российской Федерации или законом, для осуществления и выполнения возложенных
                        законодательством Российской Федерации на Оператора функций, полномочий и обязанностей;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработка персональных данных необходима для осуществления правосудия, исполнения судебного
                        акта, акта другого органа или должностного лица, подлежащих исполнению в соответствии с
                        законодательством Российской Федерации об исполнительном производстве;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработка персональных данных необходима для исполнения договора, стороной которого либо
                        выгодоприобретателем или поручителем по которому является субъект персональных данных, а также
                        для заключения договора по инициативе субъекта персональных данных или договора, по которому
                        субъект персональных данных будет являться выгодоприобретателем или поручителем;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработка персональных данных необходима для осуществления прав и законных интересов Оператора
                        или третьих лиц либо для достижения общественно значимых целей при условии, что при этом не
                        нарушаются права и свободы субъекта персональных данных;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - осуществляется обработка персональных данных, доступ неограниченного круга лиц к которым
                        предоставлен субъектом персональных данных либо по его просьбе (далее - общедоступные
                        персональные данные);</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - осуществляется обработка персональных данных, подлежащих опубликованию или обязательному
                        раскрытию в соответствии с федеральным законом.</p>
                    <h2 class="western" style="line-height:150%;" align="justify"><a
                            name="ll-__RefHeading__324_1106078070"></a><a name="ll-_Toc430593484"></a><br>
                        2.3 <font size="4">Конфиденциальность персональных данных </font><br>
                    </h2><br>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Оператор и иные лица, получившие доступ к персональным данным, обязаны не раскрывать третьим
                        лицам и не распространять персональные данные без согласия субъекта персональных данных, если
                        иное не предусмотрено федеральным законом.</p>
                    <h2 class="western" style="line-height:150%;" align="justify"><a
                            name="ll-__RefHeading__326_1106078070"></a><a name="ll-_Toc430593485"></a><br>
                        2.4 <font size="4">Общедоступные источники персональных данных</font></h2><br>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        В целях информационного обеспечения у Оператора могут создаваться общедоступные источники
                        персональных данных субъектов персональных данных, в том числе справочники и адресные книги. В
                        общедоступные источники персональных данных с письменного согласия субъекта персональных данных
                        могут включаться его фамилия, имя, отчество, дата и место рождения, должность, номера контактных
                        телефонов, адрес электронной почты и иные персональные данные, сообщаемые субъектом персональных
                        данных.</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Сведения о субъекте персональных данных должны быть в любое время исключены из общедоступных
                        источников персональных данных по требованию субъекта персональных данных, уполномоченного
                        органа по защите прав субъектов персональных данных либо по решению суда.</p>
