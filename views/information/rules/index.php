                <div class="blogpost">
                    <p align="center">
                        1.&nbsp;ОБЩИЕ ПОЛОЖЕНИЯ
                    </p>
                    <p align="center">&nbsp;</p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        1.1.&nbsp;Муниципальное учреждение культуры муниципального образования город Краснодар
                        «Централизованная библиотечная система города Краснодара» Библиотека им. Л. Н. Толстого (филиал № 18). &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(далее&nbsp;–&nbsp;Библиотека)
                        является некоммерческим учреждением, осуществляющим информационную,
                        культурно-просветительскую деятельность, располагающим организованным фондом тиражированных
                        документов (печатных, электронных, аудиовизуальных), предоставляющим их во временное
                        пользование физическим и юридическим лицам.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        Библиотека является специализированной государственной библиотекой, обеспечивающей
                        библиотечно-информационное обслуживание юношества и молодежи на территории Краснодарского
                        края, их родителей, физических и юридических лиц, профессионально занимающихся проблемами
                        молодежи, а также иных лиц.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        1.2.&nbsp;Библиотека в своей деятельности руководствуется Конституцией Российской Федерации,
                        Гражданским кодексом Российской Федерации, Указом Президента Российской Федерации от
                        24.12.2014 № 808 "Об утверждении Основ государственной культурной политики", Федеральным
                        законом Российской Федерации от 29.12.1994 № 78-ФЗ "О библиотечном деле", Федеральным
                        законом Российской Федерации от 27.07.2006 № 149-ФЗ "Об информации, информационных
                        технологиях и о защите информации", Федеральным законом Российской Федерации от 29.12. 2010
                        № 436-ФЗ &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"О защите детей от
                        информации, приносящей вред их здоровью и развитию", Федеральным законом Российской
                        Федерации от 27.07.2006 № 152-ФЗ &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"О
                        персональных данных", Законом Краснодарского края от 23.04.1996 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;№
                        28-КЗ "О библиотечном деле в Краснодарском крае", иными законодательными актами Российской
                        Федерации и Краснодарского края, Уставом государственного бюджетного учреждения культуры
                        Краснодарского края Муниципальное учреждение культуры муниципального образования город Краснодар «Централизованная библиотечная система города Краснодара» Библиотека им. Л. Н. Толстого (филиал № 18).,
                        настоящими Правилами и другими нормативными документами.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        1.3.&nbsp;Библиотека выполняет государственное задание, сформированное и утверждённое
                        министерством культуры Краснодарского края (далее – Учредитель) в соответствии с
                        предусмотренными её Уставом основными видами деятельности.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        1.4.&nbsp;Библиотека реализует идею "открытого пространства". Её фонды, ресурсы и услуги
                        максимально доступны всем гражданам независимо от пола, возраста, национальности,
                        образования, социального положения, политических и религиозных убеждений в соответствии с
                        действующим законодательством Российской Федерации.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        1.4.2.&nbsp;Обязательная регистрация и получение читательского билета в Библиотеке
                        необходимы в случаях:
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        –&nbsp;получения документов во временное пользование на дом и в помещении библиотеки;
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        – получения доступа к базам данных в Библиотеке;
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        – получения информационно-компьютерных услуг;
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        – пользования wi-fi;
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        – пользования библиотечным оборудованием, предназначенным для пользователей, в залах
                        обслуживания.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        1.4.3.&nbsp;Не обязательны регистрация и наличие читательского билета в случаях:
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        – участия в библиотечных мероприятиях;
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        – получения дополнительных (платных) услуг (светокопирование, ламинирование и т.п.)
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        1.4.4.&nbsp;Запись (перерегистрация) пользователей прекращается за 15 минут до окончания
                        работы Библиотеки. Приём заказов на выдачу документов прекращается за 10 минут до окончания
                        работы Библиотеки. Приём денежных средств за оказанные платные услуги в кассу Библиотеки
                        прекращается за 10 минут до окончания работы Библиотеки.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        1.5.&nbsp;Настоящие Правила пользования, а также изменения и дополнения к ним, по
                        согласованию с Учредителем, утверждаются директором Библиотеки и являются обязательными к
                        исполнению.
                    </p>
                    <p>
                        &nbsp;
                    </p>
                    <p align="center">
                        2.&nbsp;ПОРЯДОК ЗАПИСИ В БИБЛИОТЕКУ
                    </p>
                    <p align="center">
                        &nbsp;
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        2.1.&nbsp;Запись в библиотеку осуществляется в секторе записи читателей.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        Читателям 14 лет и старше для проведения записи в Библиотеку, оформления читательского
                        билета, читательского формуляра и заполнения регистрационной карточки необходимо предъявить
                        паспорт и фотографию (3*4) и сообщить дополнительные сведения об образовании, профессии,
                        месте работы и (или) учебы, контактные телефоны, адрес электронной почты, адрес фактического
                        места жительства.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        Несовершеннолетние граждане (до 14 лет) записываются в Библиотеку на основании письменного
                        поручительства и документов, удостоверяющих личность родителей или лиц, законно
                        представляющих его интересы.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        2.2.&nbsp;При записи пользователь знакомится с Правилами пользования, расписывается в
                        регистрационной карточке, чем подтверждает обязательство о выполнении настоящих Правил
                        пользования и согласие на автоматизированную и неавтоматизированную обработку своих
                        персональных данных.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        2.3.&nbsp;Запись проводится в автоматизированном режиме. Пользователь получает читательский
                        билет со штриховым кодом, дающий право на обслуживание во всех отделах Библиотеки. Запись в
                        Библиотеку и получение читательского билета требуют личного присутствия пользователя.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        2.4.&nbsp;Ежегодная перерегистрация пользователей производится после сдачи ими в Библиотеку
                        всех находящихся во временном пользовании документов и при наличии паспорта.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        2.5.&nbsp;Выдача читательского билета осуществляется бесплатно.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        2.6. При отсутствии читательского билета и документов, удостоверяющих личность, пользователь
                        может получить документы из библиотечного фонда во временное пользование в режиме читального
                        зала в течение одного дня по разовому читательскому билету.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        2.7.&nbsp;Читательский билет даёт право только личного пользования фондами и оборудованием
                        Библиотеки и не может быть передан другому лицу. В случае утери читательского билета
                        пользователю при предъявлении паспорта выдаётся дубликат. Восстановление билета
                        осуществляется бесплатно.
                    </p>
                    <p align="center">
                        &nbsp;
                    </p>
                    <p align="center">
                        3. ПРАВА, ОБЯЗАННОСТИ И ОТВЕТСТВЕННОСТЬ ПОЛЬЗОВАТЕЛЕЙ БИБЛИОТЕКИ
                    </p>
                    <p align="center">
                        &nbsp;
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        3.1.&nbsp;Пользователи имеют право:
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        3.1.1.&nbsp;Бесплатно получать:
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        –&nbsp;информацию о составе фондов Библиотеки и наличии конкретного документа;
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        –&nbsp;консультационную помощь в поиске и выборе источников информации;
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        –&nbsp;консультационную помощь в пользовании электронным и иным оборудованием;
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        –&nbsp;документы из фондов Библиотеки во временное пользование на дом и в помещении
                        библиотеки, а также пользоваться основными бесплатными услугами.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        3.1.2. Получать дополнительные (на платной основе) услуги согласно Положению о платных
                        услугах Библиотеки и Прейскуранту на платные услуги, предоставляемые Библиотекой.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        3.1.2.&nbsp;Пользоваться:
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        –&nbsp;ресурсами и сервисами Интернет, лицензионными базами данных и электронными версиями
                        изданий, доступ к которым организован в Библиотеке в соответствии с лицензионными
                        соглашениями;
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        –&nbsp;техническими средствами Библиотеки (информационный киоск, аудио-, видеосистема и
                        т.п.), специально установленными для пользователей, при условии соблюдения соответствующих
                        правил пользования;
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        3.1.3.&nbsp;Использовать в помещениях Библиотеки личные технические средства без вспышки и
                        звуковых сигналов.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        3.1.4.&nbsp;Участвовать в культурно-информационных мероприятиях, проводимых Библиотекой.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        3.1.5.&nbsp;Высказывать администрации письменно или устно свое мнение о работе Библиотеки,
                        вносить предложения, направленные на повышение качества обслуживания пользователей
                        Библиотеки.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        3.1.6.&nbsp;Входить в состав общественных советов, творческих объединений, совместно с
                        Библиотекой создавать клубы и кружки по интересам, сообщества друзей Библиотеки и
                        участвовать в других формах коллективной деятельности.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        3.1.7.&nbsp;Осуществлять возврат ранее взятых на дом документов из фонда Библиотеки без
                        предъявления читательского билета.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        3.1.8.&nbsp;Права юношей и девушек от 15 до 30 лет на библиотечное обслуживание приоритетны
                        относительно к правам иных физических и юридических лиц (Федеральный закон "О библиотечном
                        деле" №&nbsp;78-ФЗ ст.8.п.4).
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        3.1.9.&nbsp;Пользователи юношеского возраста (от 15 до 30 лет) с ограниченными возможностями
                        жизнедеятельности могут получать документы из фонда путём индивидуального обслуживания на
                        дому.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        3.1.10.&nbsp;Инвалиды юношеского возраста (от 15 до 30 лет) имеют право на обслуживание вне
                        очереди и получение услуги "Предоставление компьютерного времени" бесплатно.
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        3.2.&nbsp;Пользователи Библиотеки обязаны:
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        –&nbsp;соблюдать настоящие Правила пользования;
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
                        –&nbsp;предоставлять достоверные личные сведения при записи в Библиотеку и перерегистрации;
                    </p>
                    <p style="text-align: justify; text-indent:1.25cm;margin-top:.21cm;">
