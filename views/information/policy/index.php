                        2) <font size="4">ПРИНЦИПЫ И УСЛОВИЯ ОБРАБОТКИ ПЕРСОНАЛЬНЫХ ДАННЫХ</font></h1><br>
                    <h2 class="western" style="line-height:150%;" align="justify"><a
                            name="ll-__RefHeading__320_1106078070"></a><a name="ll-_Toc430593482"></a><br>
                        2.1 <font size="4">Принципы обработки персональных данных</font></h2><br>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Обработка персональных данных у Оператора осуществляется на основе следующих принципов: <br>
                    </p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - законности и справедливой основы;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - ограничения обработки персональных данных достижением конкретных, заранее определенных и
                        законных целей;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - недопущения обработки персональных данных, несовместимой с целями сбора персональных данных;
                    </p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - недопущения объединения баз данных, содержащих персональные данные, обработка которых
                        осуществляется в целях, несовместимых между собой;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработки только тех персональных данных, которые отвечают целям их обработки;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - соответствия содержания и объема обрабатываемых персональных данных заявленным целям
                        обработки;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - недопущения обработки персональных данных, избыточных по отношению к заявленным целям их
                        обработки;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обеспечения точности, достаточности и актуальности персональных данных по отношению к целям
                        обработки персональных данных; <br>
                    </p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - уничтожения либо обезличивания персональных данных по достижении целей их обработки или в
                        случае утраты необходимости в достижении этих целей, при невозможности устранения Оператором
                        допущенных нарушений персональных данных, если иное не предусмотрено федеральным законом.</p>
                    <h2 class="western" style="line-height:150%;" align="justify"><a
