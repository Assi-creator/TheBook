                            name="ll-__RefHeading__328_1106078070"></a><a name="ll-_Toc430593486"></a><br>
                        2.5 <font size="4">Специальные категории персональных данных</font></h2><br>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Обработка Оператором специальных категорий персональных данных, касающихся расовой, национальной
                        принадлежности, политических взглядов, религиозных или философских убеждений, состояния
                        здоровья, интимной жизни, допускается в случаях, если:</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - субъект персональных данных дал согласие в письменной форме на обработку своих персональных
                        данных;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - персональные данные сделаны общедоступными субъектом персональных данных; <br>
                    </p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработка персональных данных осуществляется в соответствии с законодательством о
                        государственной социальной помощи, трудовым законодательством, законодательством Российской
                        Федерации о пенсиях по государственному пенсионному обеспечению, о трудовых пенсиях;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработка персональных данных необходима для защиты жизни, здоровья или иных жизненно важных
                        интересов субъекта персональных данных либо жизни, здоровья или иных жизненно важных интересов
                        других лиц и получение согласия субъекта персональных данных невозможно;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработка персональных данных осуществляется в медико-профилактических целях, в целях
                        установления медицинского диагноза, оказания медицинских и медико-социальных услуг при условии,
                        что обработка персональных данных осуществляется лицом, профессионально занимающимся медицинской
                        деятельностью и обязанным в соответствии с законодательством Российской Федерации сохранять
                        врачебную тайну;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработка персональных данных необходима для установления или осуществления прав субъекта
                        персональных данных или третьих лиц, а равно и в связи с осуществлением правосудия; <br>
                    </p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработка персональных данных осуществляется в соответствии с законодательством об
                        обязательных видах страхования, со страховым законодательством. <br>
                    </p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Обработка специальных категорий персональных данных, осуществлявшаяся в случаях, предусмотренных
                        пунктом 4 статьи 10 ФЗ-152 должна быть незамедлительно прекращена, если устранены причины,
                        вследствие которых осуществлялась их обработка, если иное не установлено федеральным законом.
                    </p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Обработка персональных данных о судимости может осуществляться Оператором исключительно в
                        случаях и в порядке, которые определяются в соответствии с федеральными законами.</p>
                    <h2 class="western" style="line-height:150%;" align="justify"><a
                            name="ll-__RefHeading__330_1106078070"></a><a name="ll-_Toc430593487"></a><br>
                        2.6 <font size="4">Биометрические персональные данные</font></h2><br>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Сведения, которые характеризуют физиологические и биологические особенности человека, на
                        основании которых можно установить его личность - биометрические персональные данные - могут
                        обрабатываться Оператором только при наличии согласия субъекта персональных данных в письменной
                        форме.</p>
                    <h2 class="western" style="line-height:150%;" align="justify"><a
                            name="ll-__RefHeading__332_1106078070"></a><a name="ll-_Toc430593488"></a><br>
                        2.7 <font size="4">Поручение обработки персональных данных другому лицу</font></h2><br>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Оператор вправе поручить обработку персональных данных другому лицу с согласия субъекта
                        персональных данных, если иное не предусмотрено федеральным законом, на основании заключаемого с
                        этим лицом договора. Лицо, осуществляющее обработку персональных данных по поручению Оператора,
                        обязано соблюдать принципы и правила обработки персональных данных, предусмотренные ФЗ-152 и
                        настоящей Политикой.</p>
                    <h2 class="western" style="line-height:150%;" align="justify"><a></a><a></a><br>
                        2.8 <font size="4">Обработка персональных данных граждан Российской Федерации</font></h2><br>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        В соответствии со статьей 2 Федерального закона от 21 июля 2014 года №242-ФЗ «О внесении
                        изменений в отдельные законодательные акты Российской Федерации в части уточнения порядка
                        обработки персональных данных в информационно-телекоммуникационных сетях» при сборе персональных
                        данных, в том числе посредством информационно-телекоммуникационной сети "Интернет", оператор
                        обязан обеспечить запись, систематизацию, накопление, хранение, уточнение (обновление,
                        изменение), извлечение персональных данных граждан Российской Федерации с использованием баз
                        данных, находящихся на территории Российской Федерации, за исключением случаев:</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработка персональных данных необходима для достижения целей, предусмотренных международным
                        договором Российской Федерации или законом, для осуществления и выполнения возложенных
                        законодательством Российской Федерации на Оператора функций, полномочий и обязанностей;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработка персональных данных необходима для осуществления правосудия, исполнения судебного
                        акта, акта другого органа или должностного лица, подлежащих исполнению в соответствии с
                        законодательством Российской Федерации об исполнительном производстве (далее - исполнение
                        судебного акта);</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработка персональных данных необходима для исполнения полномочий федеральных органов
                        исполнительной власти, органов государственных внебюджетных фондов, исполнительных органов
                        государственной власти субъектов Российской Федерации, органов местного самоуправления и функций
                        организаций, участвующих в предоставлении соответственно государственных и муниципальных услуг,
                        предусмотренных Федеральным законом от&nbsp;27 июля 2010 года №210-ФЗ&nbsp;«Об организации
                        предоставления государственных и муниципальных услуг», включая регистрацию субъекта персональных
                        данных на едином портале государственных и муниципальных услуг и (или) региональных порталах
                        государственных и муниципальных услуг;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - обработка персональных данных необходима для осуществления профессиональной деятельности
                        журналиста и (или) законной деятельности средства массовой информации либо научной, литературной
                        или иной творческой деятельности при условии, что при этом не нарушаются права и законные
                        интересы субъекта персональных данных.</p>
                    <h2 class="western" style="line-height:150%;" align="justify"><a></a><a></a><br>
                        2.9 <font size="4">Трансграничная передача персональных данных</font></h2><br>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Оператор обязан убедиться в том, что иностранным государством, на территорию которого
                        предполагается осуществлять передачу персональных данных, обеспечивается адекватная защита прав
                        субъектов персональных данных, до начала осуществления такой передачи.</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Трансграничная передача персональных данных на территории иностранных государств, не
                        обеспечивающих адекватной защиты прав субъектов персональных данных, может осуществляться в
                        случаях:</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - наличия согласия в письменной форме субъекта персональных данных на трансграничную передачу
                        его персональных данных;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - исполнения договора, стороной которого является субъект персональных данных.</p>
                    <h1 class="western" style="line-height:150%;" align="justify"><a></a><a></a><br>
                        3) <font size="4">ПРАВА СУБЪЕКТА ПЕРСОНАЛЬНЫХ ДАННЫХ</font></h1><br>
                    <h2 class="western" style="line-height:150%;" align="justify"><a></a><a></a><br>
                        3.1 <font size="4">Согласие субъекта персональных данных на обработку его персональных
                            данных</font></h2><br>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Субъект персональных данных принимает решение о предоставлении его персональных данных и дает
                        согласие на их обработку свободно, своей волей и в своем интересе. Согласие на обработку
                        персональных данных может быть дано субъектом персональных данных или его представителем в любой
                        позволяющей подтвердить факт его получения форме, если иное не установлено федеральным
                        законом.<br>
                    </p>
                    <h2 class="western" style="line-height:150%;" align="justify"><a></a><a></a><br>
                        3.2 <font size="4">Права субъекта персональных данных</font></h2><br>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Субъект персональных данных имеет право на получение у Оператора информации, касающейся
                        обработки его персональных данных, если такое право не ограничено в соответствии с федеральными
                        законами. Субъект персональных данных вправе требовать от Оператора уточнения его персональных
                        данных, их блокирования или уничтожения в случае, если персональные данные являются неполными,
                        устаревшими, неточными, незаконно полученными или не являются необходимыми для заявленной цели
                        обработки, а также принимать предусмотренные законом меры по защите своих прав.</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Обработка персональных данных в целях продвижения товаров, работ, услуг на рынке путем
                        осуществления прямых контактов с субъектом персональных данных (потенциальным потребителем) с
                        помощью средств связи, а также в целях политической агитации допускается только при условии
                        предварительного согласия субъекта персональных данных. </p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Оператор обязан немедленно прекратить по требованию субъекта персональных данных обработку его
                        персональных данных в вышеуказанных целях.</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Запрещается принятие на основании исключительно автоматизированной обработки персональных данных
                        решений, порождающих юридические последствия в отношении субъекта персональных данных или иным
                        образом затрагивающих его права и законные интересы, за исключением случаев, предусмотренных
                        федеральными законами, или при наличии согласия в письменной форме субъекта персональных данных.
                    </p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Если субъект персональных данных считает, что Оператор осуществляет обработку его персональных
                        данных с нарушением требований ФЗ-152 или иным образом нарушает его права и свободы, субъект
                        персональных данных вправе обжаловать действия или бездействие Оператора в Уполномоченный орган
                        по защите прав субъектов персональных данных или в судебном порядке.</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Субъект персональных данных имеет право на защиту своих прав и законных интересов, в том числе
                        на возмещение убытков и (или) компенсацию морального вреда.</p>
                    <h1 class="western" style="line-height:150%;" align="justify"><a></a><a></a><br>
                        4) <font size="4">ОБЕСПЕЧЕНИЕ БЕЗОПАСНОСТИ ПЕРСОНАЛЬНЫХ ДАННЫХ</font></h1><br>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Безопасность персональных данных, обрабатываемых Оператором, обеспечивается реализацией
                        правовых, организационных и технических мер, необходимых для обеспечения требований федерального
                        законодательства в области защиты персональных данных.</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Для предотвращения несанкционированного доступа к персональным данным Оператором применяются
                        следующие организационно-технические меры:</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - назначение должностных лиц, ответственных за организацию обработки и защиты персональных
                        данных;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - ограничение состава лиц, допущенных к обработке персональных данных;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - ознакомление субъектов с требованиями федерального законодательства и нормативных документов
                        Оператора по обработке и защите персональных данных;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - организация учета, хранения и обращения носителей, содержащих информацию с персональными
                        данными;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - определение угроз безопасности персональных данных при их обработке, формирование на их основе
                        моделей угроз;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - разработка на основе модели угроз системы защиты персональных данных;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - проверка готовности и эффективности использования средств защиты информации;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - разграничение доступа пользователей к информационным ресурсам и программно-аппаратным
                        средствам обработки информации;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - регистрация и учет действий пользователей информационных систем персональных данных;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - использование антивирусных средств и средств восстановления системы защиты персональных
                        данных;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - применение в необходимых случаях средств межсетевого экранирования, обнаружения вторжений,
                        анализа защищенности и средств криптографической защиты информации;</p>
                    <p style="margin-left:2.25cm;text-indent:-1cm;margin-top:.21cm;line-height:150%;" align="justify">
                        <br>
                        - организация пропускного режима на территорию Оператора, охраны помещений с техническими
                        средствами обработки персональных данных.</p>
                    <h1 class="western" style="line-height:150%;" align="justify"><a></a><a></a><br>
                        5) <font size="4">ЗАКЛЮЧИТЕЛЬНЫЕ ПОЛОЖЕНИЯ</font></h1><br>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Иные права и обязанности Оператора в связи с обработкой персональных данных определяются
                        законодательством Российской Федерации в области персональных данных.</p>
                    <p style="text-indent:1.25cm;margin-top:.21cm;line-height:150%;" align="justify"><br>
                        Работники Оператора, виновные в нарушении норм, регулирующих обработку и защиту персональных
                        данных, несут материальную, дисциплинарную, административную, гражданско-правовую или уголовную
                        ответственность в порядке, установленном федеральными законами.</p>
